CREATE OR REPLACE VIEW VW_DASHBOARD_SUMMARY AS
WITH sop AS (
  SELECT
    COUNT(*) AS TOTAL_SOPS,
    SUM(IFF(STATUS = 'Approved', 1, 0)) AS APPROVED_SOPS,
    SUM(IFF(STATUS = 'Submitted', 1, 0)) AS PENDING_SOPS,
    SUM(IFF(STATUS = 'Active', 1, 0)) AS ACTIVE_SOPS
  FROM SOPS
),
impl AS (
  SELECT
    ROUND(100.0 * SUM(IFF(STATUS IN ('Implemented', 'Verified'), 1, 0)) / NULLIF(COUNT(*), 0), 2) AS SOP_IMPLEMENTATION_PERCENT
  FROM SOP_ASSIGNMENTS
),
ca AS (
  SELECT
    SUM(IFF(STATUS IN ('Open', 'In Progress', 'Submitted'), 1, 0)) AS OPEN_CORRECTIVE_ACTIONS,
    SUM(IFF(DUE_DATE < CURRENT_DATE() AND STATUS NOT IN ('Verified', 'Closed'), 1, 0)) AS OVERDUE_CORRECTIVE_ACTIONS
  FROM CORRECTIVE_ACTIONS
),
kpi AS (
  SELECT
    SUM(IFF(FINAL_STATUS = 'Green', 1, 0)) AS GREEN_KPIS,
    SUM(IFF(FINAL_STATUS = 'Amber', 1, 0)) AS AMBER_KPIS,
    SUM(IFF(FINAL_STATUS = 'Red', 1, 0)) AS RED_KPIS
  FROM VW_KPI_STATUS_DETAIL
  WHERE REPORTING_MONTH = (
    SELECT MAX(REPORTING_MONTH)
    FROM KPI_RESULTS
  )
),
audit AS (
  SELECT
    ROUND(AVG(SCORE_PERCENT), 2) AS FOOD_SAFETY_AUDIT_SCORE
  FROM AUDITS
  WHERE STATUS = 'Completed'
    AND COMPLIANCE_AREA = 'Food Safety Compliance'
)
SELECT
  COALESCE(sop.TOTAL_SOPS, 0) AS TOTAL_SOPS,
  COALESCE(sop.APPROVED_SOPS, 0) AS APPROVED_SOPS,
  COALESCE(sop.PENDING_SOPS, 0) AS PENDING_SOPS,
  COALESCE(sop.ACTIVE_SOPS, 0) AS ACTIVE_SOPS,
  COALESCE(impl.SOP_IMPLEMENTATION_PERCENT, 0) AS SOP_IMPLEMENTATION_PERCENT,
  COALESCE(ca.OPEN_CORRECTIVE_ACTIONS, 0) AS OPEN_CORRECTIVE_ACTIONS,
  COALESCE(ca.OVERDUE_CORRECTIVE_ACTIONS, 0) AS OVERDUE_CORRECTIVE_ACTIONS,
  COALESCE(kpi.GREEN_KPIS, 0) AS GREEN_KPIS,
  COALESCE(kpi.AMBER_KPIS, 0) AS AMBER_KPIS,
  COALESCE(kpi.RED_KPIS, 0) AS RED_KPIS,
  COALESCE(audit.FOOD_SAFETY_AUDIT_SCORE, 0) AS FOOD_SAFETY_AUDIT_SCORE
FROM sop
CROSS JOIN impl
CROSS JOIN ca
CROSS JOIN kpi
CROSS JOIN audit;
