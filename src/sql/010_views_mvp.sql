SELECT
  CASE
    WHEN s.DEPARTMENT_ID IS NOT NULL AND s.LOCATION_ID IS NOT NULL THEN 'BRAND_DEPARTMENT_LOCATION'
    WHEN s.DEPARTMENT_ID IS NOT NULL THEN 'BRAND_DEPARTMENT'
    WHEN s.LOCATION_ID IS NOT NULL THEN 'BRAND_LOCATION'
    ELSE 'BRAND'
