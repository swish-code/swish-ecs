CREATE OR REPLACE VIEW VW_OVERDUE_ACTIONS AS
SELECT
  ca.ACTION_ID,
  ca.SOURCE_TYPE,
  ca.SOURCE_ID,
  ca.TITLE,
  ca.DESCRIPTION,
  CASE
    WHEN ca.DEPARTMENT_ID IS NOT NULL AND ca.LOCATION_ID IS NOT NULL THEN 'BRAND_DEPARTMENT_LOCATION'
    WHEN ca.DEPARTMENT_ID IS NOT NULL THEN 'BRAND_DEPARTMENT'
    WHEN ca.LOCATION_ID IS NOT NULL THEN 'BRAND_LOCATION'
    ELSE 'BRAND'
  END AS SCOPE_LEVEL,
  ca.BRAND_ID,
  b.BRAND_CODE,
  b.BRAND_NAME,
  ca.DEPARTMENT_ID,
  d.DEPARTMENT_CODE,
  d.DEPARTMENT_NAME,
  ca.LOCATION_ID,
  l.LOCATION_CODE,
  l.LOCATION_NAME,
  ca.ASSIGNED_TO,
  u.DISPLAY_NAME AS ASSIGNED_TO_NAME,
  ca.SEVERITY,
  ca.STATUS,
  ca.DUE_DATE,
  DATEDIFF('DAY', ca.DUE_DATE, CURRENT_DATE()) AS DAYS_OVERDUE,
  ca.REMARKS,
  ca.CREATED_AT,
  ca.UPDATED_AT
FROM CORRECTIVE_ACTIONS ca
LEFT JOIN BRANDS b ON b.BRAND_ID = ca.BRAND_ID
LEFT JOIN DEPARTMENTS d ON d.DEPARTMENT_ID = ca.DEPARTMENT_ID
LEFT JOIN LOCATIONS l ON l.LOCATION_ID = ca.LOCATION_ID
LEFT JOIN USERS u ON u.USER_ID = ca.ASSIGNED_TO
WHERE ca.DUE_DATE < CURRENT_DATE()
  AND ca.STATUS NOT IN ('Verified', 'Closed');

CREATE OR REPLACE VIEW VW_KPI_STATUS_DETAIL AS
SELECT
  r.KPI_RESULT_ID,
  r.KPI_ID,
  d.KPI_NAME,
  CASE
    WHEN COALESCE(r.DEPARTMENT_ID, d.DEPARTMENT_ID) IS NOT NULL AND COALESCE(r.LOCATION_ID, d.LOCATION_ID) IS NOT NULL THEN 'BRAND_DEPARTMENT_LOCATION'
    WHEN COALESCE(r.DEPARTMENT_ID, d.DEPARTMENT_ID) IS NOT NULL THEN 'BRAND_DEPARTMENT'
    WHEN COALESCE(r.LOCATION_ID, d.LOCATION_ID) IS NOT NULL THEN 'BRAND_LOCATION'
    ELSE 'BRAND'
  END AS SCOPE_LEVEL,
  COALESCE(r.BRAND_ID, d.BRAND_ID) AS BRAND_ID,
  b.BRAND_CODE,
  b.BRAND_NAME,
  COALESCE(r.DEPARTMENT_ID, d.DEPARTMENT_ID) AS DEPARTMENT_ID,
  dept.DEPARTMENT_CODE,
  dept.DEPARTMENT_NAME,
  COALESCE(r.LOCATION_ID, d.LOCATION_ID) AS LOCATION_ID,
  l.LOCATION_CODE,
  l.LOCATION_NAME,
  r.REPORTING_MONTH,
  d.TARGET_VALUE,
  d.TARGET_DIRECTION,
  r.ACTUAL_VALUE,
  COALESCE(
    r.STATUS,
    CASE
      WHEN d.TARGET_VALUE IS NULL THEN NULL
      WHEN d.TARGET_DIRECTION = '>=' AND r.ACTUAL_VALUE >= d.TARGET_VALUE THEN 'Green'
      WHEN d.TARGET_DIRECTION = '>=' AND r.ACTUAL_VALUE >= d.TARGET_VALUE * 0.95 THEN 'Amber'
      WHEN d.TARGET_DIRECTION = '<=' AND r.ACTUAL_VALUE <= d.TARGET_VALUE THEN 'Green'
      WHEN d.TARGET_DIRECTION = '<=' AND r.ACTUAL_VALUE <= d.TARGET_VALUE * 1.05 THEN 'Amber'
      ELSE 'Red'
    END
  ) AS FINAL_STATUS,
  r.COMMENTS,
  r.CREATED_AT
FROM KPI_RESULTS r
JOIN KPI_DEFINITIONS d ON d.KPI_ID = r.KPI_ID
LEFT JOIN BRANDS b ON b.BRAND_ID = COALESCE(r.BRAND_ID, d.BRAND_ID)
LEFT JOIN DEPARTMENTS dept ON dept.DEPARTMENT_ID = COALESCE(r.DEPARTMENT_ID, d.DEPARTMENT_ID)
LEFT JOIN LOCATIONS l ON l.LOCATION_ID = COALESCE(r.LOCATION_ID, d.LOCATION_ID);

CREATE OR REPLACE VIEW VW_KPI_STATUS_SUMMARY AS
SELECT
  REPORTING_MONTH,
  FINAL_STATUS AS STATUS,
  COUNT(*) AS KPI_COUNT
FROM VW_KPI_STATUS_DETAIL
GROUP BY REPORTING_MONTH, FINAL_STATUS;

CREATE OR REPLACE VIEW VW_AUDIT_SCORE_SUMMARY AS
SELECT
  DATE_TRUNC('MONTH', COALESCE(COMPLETED_AT, STARTED_AT))::DATE AS AUDIT_MONTH,
  COUNT(*) AS AUDIT_COUNT,
  ROUND(AVG(SCORE_PERCENT), 2) AS AVG_SCORE_PERCENT
FROM AUDITS
WHERE STATUS = 'Completed'
GROUP BY DATE_TRUNC('MONTH', COALESCE(COMPLETED_AT, STARTED_AT))::DATE;

