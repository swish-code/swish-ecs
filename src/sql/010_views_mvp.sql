CREATE OR REPLACE VIEW VW_SOP_IMPLEMENTATION_SUMMARY AS
SELECT
  COUNT(*) AS TOTAL_ASSIGNMENTS,
  SUM(IFF(STATUS IN ('Implemented', 'Verified'), 1, 0)) AS IMPLEMENTED_OR_VERIFIED,
  SUM(IFF(STATUS = 'Verified', 1, 0)) AS VERIFIED_COUNT,
  ROUND(100.0 * SUM(IFF(STATUS IN ('Implemented', 'Verified'), 1, 0)) / NULLIF(COUNT(*), 0), 2) AS IMPLEMENTED_PERCENT,
  ROUND(100.0 * SUM(IFF(STATUS = 'Verified', 1, 0)) / NULLIF(COUNT(*), 0), 2) AS VERIFIED_PERCENT
FROM SOP_ASSIGNMENTS;

