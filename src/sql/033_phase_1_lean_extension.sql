   ========================================================= */

ALTER TABLE AUDITS ADD COLUMN IF NOT EXISTS PROGRAM_ID NUMBER(38,0);
ALTER TABLE AUDITS ADD COLUMN IF NOT EXISTS AUDIT_TYPE VARCHAR(30);

