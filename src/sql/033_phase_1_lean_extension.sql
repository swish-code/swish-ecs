   12. ALTER CORRECTIVE_ACTIONS
   ========================================================= */

ALTER TABLE CORRECTIVE_ACTIONS ADD COLUMN IF NOT EXISTS FINDING_ID NUMBER(38,0);
ALTER TABLE CORRECTIVE_ACTIONS ADD COLUMN IF NOT EXISTS CONTROL_ID NUMBER(38,0);

