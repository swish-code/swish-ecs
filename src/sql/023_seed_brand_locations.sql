USE DATABASE SWISH_DOCUMENTS;
USE SCHEMA ECS_MVP;

MERGE INTO BRAND_LOCATIONS target
USING (
  SELECT
    b.BRAND_ID,
    l.LOCATION_ID
  FROM (
    SELECT 'YP' AS BRAND_CODE, 'DHY' AS LOCATION_CODE
  UNION ALL SELECT 'BBT', 'ADL'
  UNION ALL SELECT 'PAT', 'ADL'
  UNION ALL SELECT 'SLC', 'ADL'
  UNION ALL SELECT 'YP', 'ADL'
  UNION ALL SELECT 'TBL', 'AHM'
  UNION ALL SELECT 'YP', 'AND'
  UNION ALL SELECT 'BBT', 'ARD'
  UNION ALL SELECT 'MM', 'ARD'
  UNION ALL SELECT 'PAT', 'ARD'
  UNION ALL SELECT 'SLC', 'ARD'
  UNION ALL SELECT 'SS', 'ARD'
  UNION ALL SELECT 'TBL', 'ARD'
  UNION ALL SELECT 'YP', 'ARD'
  UNION ALL SELECT 'BBT', 'BYN'
  UNION ALL SELECT 'SS', 'BYN'
  UNION ALL SELECT 'TBL', 'BNQ'
  UNION ALL SELECT 'SS', 'AGL'
  UNION ALL SELECT 'YP', 'AGL'
  UNION ALL SELECT 'YP', 'FHL'
  UNION ALL SELECT 'CHP', 'HWL'
  UNION ALL SELECT 'PAT', 'HWL'
  UNION ALL SELECT 'SS', 'HWL'
  UNION ALL SELECT 'TBL', 'HWL'
  UNION ALL SELECT 'YP', 'ISH'
  UNION ALL SELECT 'YP', 'JAD'
  UNION ALL SELECT 'MM', 'JBR'
  UNION ALL SELECT 'YP', 'JBR'
  UNION ALL SELECT 'BBT', 'JHR'
  UNION ALL SELECT 'MM', 'JHR'
  UNION ALL SELECT 'PAT', 'JHR'
  UNION ALL SELECT 'SLC', 'JHR'
  UNION ALL SELECT 'SS', 'JHR'
  UNION ALL SELECT 'TBL', 'JHR'
  UNION ALL SELECT 'YP', 'JHR'
  UNION ALL SELECT 'YP', 'JLB'
  UNION ALL SELECT 'MM', 'KFN'
  UNION ALL SELECT 'MM', 'KHR'
  UNION ALL SELECT 'TBL', 'KHR'
  UNION ALL SELECT 'YP', 'KHR'
  UNION ALL SELECT 'MM', 'KTN'
  UNION ALL SELECT 'BBT', 'KWT'
  UNION ALL SELECT 'SLC', 'KWT'
  UNION ALL SELECT 'SS', 'KWT'
  UNION ALL SELECT 'MM', 'MHB'
  UNION ALL SELECT 'TBL', 'MHB'
  UNION ALL SELECT 'YP', 'MID'
  UNION ALL SELECT 'BBT', 'MNF'
  UNION ALL SELECT 'MM', 'MNF'
  UNION ALL SELECT 'PAT', 'MIS'
  UNION ALL SELECT 'SLC', 'MIS'
  UNION ALL SELECT 'MM', 'QDS'
  UNION ALL SELECT 'BUR', 'QTB'
  UNION ALL SELECT 'CHP', 'QTB'
  UNION ALL SELECT 'TBL', 'QTB'
  UNION ALL SELECT 'YP', 'QTB'
  UNION ALL SELECT 'MM', 'QRN'
  UNION ALL SELECT 'SS', 'QRN'
  UNION ALL SELECT 'YP', 'QRN'
  UNION ALL SELECT 'SS', 'RAI'
  UNION ALL SELECT 'BBT', 'SAD'
  UNION ALL SELECT 'BBT', 'SBA'
  UNION ALL SELECT 'SS', 'SBA'
  UNION ALL SELECT 'YP', 'SBA'
  UNION ALL SELECT 'MM', 'SBS'
  UNION ALL SELECT 'TBL', 'SBS'
  UNION ALL SELECT 'YP', 'SBS'
  UNION ALL SELECT 'BBT', 'SAL'
  UNION ALL SELECT 'MM', 'SAL'
  UNION ALL SELECT 'PAT', 'SAL'
  UNION ALL SELECT 'SLC', 'SAL'
  UNION ALL SELECT 'SS', 'SAL'
  UNION ALL SELECT 'TBL', 'SAL'
  UNION ALL SELECT 'YP', 'SAL'
  UNION ALL SELECT 'MM', 'SLW'
  UNION ALL SELECT 'YP', 'SLW'
  UNION ALL SELECT 'BBT', 'SMY'
  UNION ALL SELECT 'BBT', 'SHD'
  UNION ALL SELECT 'YP', 'VIB'
  UNION ALL SELECT 'MM', 'WAM'
  UNION ALL SELECT 'BBT', 'YRD'
  UNION ALL SELECT 'BUR', 'YRD'
  UNION ALL SELECT 'CHP', 'YRD'
  UNION ALL SELECT 'PAT', 'YRD'
  UNION ALL SELECT 'SLC', 'YRD'
  UNION ALL SELECT 'YP', 'ZAH'
  UNION ALL SELECT 'FM', 'JBR'
  UNION ALL SELECT 'FM', 'KTN'
  UNION ALL SELECT 'FM', 'KWT'
  UNION ALL SELECT 'FM', 'YRD'
  ) source_codes
  JOIN BRANDS b
    ON b.BRAND_CODE = source_codes.BRAND_CODE
  JOIN LOCATIONS l
    ON l.LOCATION_CODE = source_codes.LOCATION_CODE
) source
ON target.BRAND_ID = source.BRAND_ID
AND target.LOCATION_ID = source.LOCATION_ID
WHEN NOT MATCHED THEN
  INSERT (BRAND_ID, LOCATION_ID)
  VALUES (source.BRAND_ID, source.LOCATION_ID);
