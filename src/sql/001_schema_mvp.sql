CREATE OR REPLACE TABLE KPI_DEFINITIONS (
  KPI_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  KPI_NAME VARCHAR(200) NOT NULL,
  BRAND_ID NUMBER(38,0),
  DEPARTMENT_ID NUMBER(38,0),
  TARGET_VALUE NUMBER(18,4),
  TARGET_DIRECTION VARCHAR(5) NOT NULL DEFAULT '>=',
  OWNER_USER_ID NUMBER(38,0),
  IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
  CREATED_BY NUMBER(38,0) NOT NULL,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
