);

CREATE OR REPLACE TABLE KPI_RESULTS (
  KPI_RESULT_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  KPI_ID NUMBER(38,0) NOT NULL,
  REPORTING_MONTH DATE NOT NULL,
  ACTUAL_VALUE NUMBER(18,4) NOT NULL,
  STATUS VARCHAR(10),
  COMMENTS VARCHAR(2000),
  CREATED_BY NUMBER(38,0) NOT NULL,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  PRIMARY KEY (KPI_RESULT_ID)
);

CREATE OR REPLACE TABLE AUDIT_LOG (
  LOG_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  ENTITY_TYPE VARCHAR(50) NOT NULL,
  ENTITY_ID NUMBER(38,0) NOT NULL,
  ACTION VARCHAR(100) NOT NULL,
  CHANGED_BY NUMBER(38,0) NOT NULL,
  DETAILS VARIANT,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  PRIMARY KEY (LOG_ID)
);

