CREATE OR REPLACE TABLE ROLES (
  ROLE_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  ROLE_KEY VARCHAR(50) NOT NULL,
  ROLE_NAME VARCHAR(100) NOT NULL,
  IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  PRIMARY KEY (ROLE_ID)
);

CREATE OR REPLACE TABLE BRANDS (
  BRAND_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  BRAND_CODE VARCHAR(50) NOT NULL,
  BRAND_NAME VARCHAR(200) NOT NULL,
  IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  UPDATED_AT TIMESTAMP_TZ,
  PRIMARY KEY (BRAND_ID)
);

CREATE OR REPLACE TABLE DEPARTMENTS (
  DEPARTMENT_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  DEPARTMENT_CODE VARCHAR(50) NOT NULL,
  DEPARTMENT_NAME VARCHAR(200) NOT NULL,
  IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  UPDATED_AT TIMESTAMP_TZ,
  PRIMARY KEY (DEPARTMENT_ID)
);

CREATE OR REPLACE TABLE USERS (
  USER_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  EMAIL VARCHAR(320) NOT NULL,
  DISPLAY_NAME VARCHAR(200) NOT NULL,
  BRAND_ID NUMBER(38,0),
  DEPARTMENT_ID NUMBER(38,0),
  IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  UPDATED_AT TIMESTAMP_TZ,
  PRIMARY KEY (USER_ID)
);

CREATE OR REPLACE TABLE USER_ROLES (
  USER_ID NUMBER(38,0) NOT NULL,
  ROLE_ID NUMBER(38,0) NOT NULL,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  PRIMARY KEY (USER_ID, ROLE_ID)
);

CREATE OR REPLACE TABLE SOPS (
  SOP_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  DOCUMENT_NO VARCHAR(50) NOT NULL,
  DOCUMENT_TYPE VARCHAR(30) NOT NULL,
  TITLE VARCHAR(300) NOT NULL,
  BRAND_ID NUMBER(38,0),
  DEPARTMENT_ID NUMBER(38,0) NOT NULL,
  COMPLIANCE_AREA VARCHAR(100) NOT NULL,
  OWNER_USER_ID NUMBER(38,0),
  VERSION_NO VARCHAR(30) NOT NULL,
  STATUS VARCHAR(30) NOT NULL,
  FILE_NAME VARCHAR(255),
  FILE_URL VARCHAR(1000),
  FILE_SIZE NUMBER(18,0),
  MIME_TYPE VARCHAR(100),
  SUBMITTED_BY NUMBER(38,0),
  SUBMITTED_AT TIMESTAMP_TZ,
  APPROVED_BY NUMBER(38,0),
  APPROVED_AT TIMESTAMP_TZ,
  APPROVAL_REMARKS VARCHAR(2000),
  ACTIVE_FROM DATE,
  NEXT_REVIEW_DATE DATE,
  REMARKS VARCHAR(2000),
  CREATED_BY NUMBER(38,0) NOT NULL,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  UPDATED_BY NUMBER(38,0),
  UPDATED_AT TIMESTAMP_TZ,
  PRIMARY KEY (SOP_ID)
);

CREATE OR REPLACE TABLE SOP_ASSIGNMENTS (
  ASSIGNMENT_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  SOP_ID NUMBER(38,0) NOT NULL,
  BRAND_ID NUMBER(38,0) NOT NULL,
  DEPARTMENT_ID NUMBER(38,0) NOT NULL,
  OWNER_USER_ID NUMBER(38,0),
  STATUS VARCHAR(30) NOT NULL,
  TARGET_DATE DATE,
