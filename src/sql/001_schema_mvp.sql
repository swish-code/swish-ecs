  VERIFIED_BY NUMBER(38,0),
  VERIFIED_AT TIMESTAMP_TZ,
  REMARKS VARCHAR(2000),
  CREATED_BY NUMBER(38,0) NOT NULL,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  UPDATED_BY NUMBER(38,0),
  UPDATED_AT TIMESTAMP_TZ,
  PRIMARY KEY (ASSIGNMENT_ID)
);

CREATE OR REPLACE TABLE CHECKLIST_TEMPLATES (
  TEMPLATE_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  TEMPLATE_NAME VARCHAR(200) NOT NULL,
  COMPLIANCE_AREA VARCHAR(100) NOT NULL,
  IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
  CREATED_BY NUMBER(38,0) NOT NULL,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  UPDATED_BY NUMBER(38,0),
  UPDATED_AT TIMESTAMP_TZ,
  PRIMARY KEY (TEMPLATE_ID)
);

CREATE OR REPLACE TABLE CHECKLIST_ITEMS (
  ITEM_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  TEMPLATE_ID NUMBER(38,0) NOT NULL,
  SECTION_NAME VARCHAR(200),
  ITEM_TEXT VARCHAR(1000) NOT NULL,
  WEIGHT NUMBER(10,2) NOT NULL DEFAULT 1,
  SORT_ORDER NUMBER(38,0) NOT NULL DEFAULT 0,
  IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  PRIMARY KEY (ITEM_ID)
);

CREATE OR REPLACE TABLE AUDITS (
  AUDIT_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  TEMPLATE_ID NUMBER(38,0) NOT NULL,
  BRAND_ID NUMBER(38,0) NOT NULL,
  DEPARTMENT_ID NUMBER(38,0) NOT NULL,
  COMPLIANCE_AREA VARCHAR(100) NOT NULL,
  STATUS VARCHAR(30) NOT NULL,
  PERFORMED_BY NUMBER(38,0) NOT NULL,
  STARTED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
  COMPLETED_AT TIMESTAMP_TZ,
  SCORE_PERCENT NUMBER(10,2),
  REMARKS VARCHAR(2000),
  PRIMARY KEY (AUDIT_ID)
);

CREATE OR REPLACE TABLE AUDIT_RESPONSES (
  RESPONSE_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  AUDIT_ID NUMBER(38,0) NOT NULL,
  ITEM_ID NUMBER(38,0) NOT NULL,
  RESULT VARCHAR(30) NOT NULL,
  COMMENTS VARCHAR(2000),
  PRIMARY KEY (RESPONSE_ID)
);

CREATE OR REPLACE TABLE CORRECTIVE_ACTIONS (
  ACTION_ID NUMBER(38,0) AUTOINCREMENT START 1 INCREMENT 1,
  SOURCE_TYPE VARCHAR(30) NOT NULL,
  SOURCE_ID NUMBER(38,0),
  TITLE VARCHAR(300) NOT NULL,
  DESCRIPTION VARCHAR(2000),
  BRAND_ID NUMBER(38,0),
  DEPARTMENT_ID NUMBER(38,0),
  ASSIGNED_TO NUMBER(38,0) NOT NULL,
  SEVERITY VARCHAR(30) NOT NULL,
  STATUS VARCHAR(30) NOT NULL,
  DUE_DATE DATE NOT NULL,
  SUBMITTED_BY NUMBER(38,0),
  SUBMITTED_AT TIMESTAMP_TZ,
  VERIFIED_BY NUMBER(38,0),
  VERIFIED_AT TIMESTAMP_TZ,
  CLOSED_AT TIMESTAMP_TZ,
  REMARKS VARCHAR(2000),
  CREATED_BY NUMBER(38,0) NOT NULL,
  CREATED_AT TIMESTAMP_TZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
